public class Increment_Decrement_5 {
    public static void main(String[] args) {
         int x=5;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        int y=6;
        ++y;
        System.out.println(y);
        --y;
        System.out.println(y);

    }
}
