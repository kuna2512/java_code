public class DoubleData_Type_6 {
    public static void main(String[] args) {
        double x=39;
        double y=14;
        double z=x/y;
        System.out.println(z);
        double a=5.0/2;
        System.out.println(a);
        double b=7/3.0;
        System.out.println(b);
        double c= 7/3;
        System.out.println(c);
        double d= 29;
        double e= 45;
        System.out.println(d+e);
        System.out.println(d-e);
        System.out.println(d*e);
        System.out.println(d/e);
        System.out.println(d%e);

    }
}
