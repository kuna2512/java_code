public class Relation_operator_27 {
    public static void main(String[] args) {
        int p = 10;
        int q = 15;
        System.out.println(p == q);
        System.out.println(p != q);
        System.out.println(p <= q);
        System.out.println(p >= q);
        System.out.println(p < q);
        System.out.println(p > q);


    }
}
