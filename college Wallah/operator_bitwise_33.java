public class operator_bitwise_33 {
    public static void main(String[] args) {
        int p= 9, q= 10;
        System.out.println(p|q);
        System.out.println(p&q);
        System.out.println(p^q);

        System.out.println(p<<2);
        System.out.println(p<<3);

        System.out.println(q>>1);
        System.out.println(q>>3);
    }
}