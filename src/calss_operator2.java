// relational operator.

public class calss_operator2 {
    public static void main(String[] args) {

        int a=10,b=20;
        System.out.println(a==b); //10==20
        System.out.println(a!=b);  //10!=20
        System.out.println(a>b);  //10>20
        System.out.println(a<b);   //10<20
        System.out.println(a>=b);  //10>=20
        System.out.println(a<=b);  //10<=20
    }
}
